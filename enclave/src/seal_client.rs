//! In-enclave Seal client (G3). Fetches IBE decryption keys from Seal key
//! servers — release is gated on-chain by `seal_policy::seal_approve`, which
//! verifies THIS enclave's signature over the requested identity — then
//! decrypts the sealed held-out set entirely in memory.
//!
//! Wire formats match @mysten/seal 1.1.3 (the SDK the repo seals with); the
//! cryptography is the canonical `crypto` crate from MystenLabs/seal — the same
//! code the key servers run — so group-element serialization is byte-compatible
//! by construction. Cross-language vectors live in fixtures/seal-vectors.json
//! (generated by tools/gen-seal-vectors.ts).
