    /// The production direction: TS SDK encrypts (as scripts/seal-and-notarize
    /// does), this Rust client decrypts with user secret keys extracted from
    /// the vector's master keys.
    #[test]
    fn decrypts_ts_sdk_encrypted_object() {
        let v = vectors();
        let e = &v["encryptedObject"];
        let bytes = B64.decode(e["encryptedObjectB64"].as_str().unwrap()).unwrap();
        let object: EncryptedObject = bcs::from_bytes(&bytes).unwrap();
        let full_id = create_full_id(object.package_id.inner(), &object.id);

        let pks: Vec<G2Element> = e["serverPksB64"]
            .as_array()
            .unwrap()
            .iter()
            .map(|pk| g2_from_slice(&B64.decode(pk.as_str().unwrap()).unwrap()).unwrap())
            .collect();
        let mut usks = HashMap::new();
        for (i, usk_b64) in e["usksB64"].as_array().unwrap().iter().enumerate() {
            if i >= object.threshold as usize {
                break;
            }
            let usk = g1_from_slice(&B64.decode(usk_b64.as_str().unwrap()).unwrap()).unwrap();
            verify_user_secret_key(&usk, &full_id, &pks[i]).unwrap();
            usks.insert(object.services[i].0, usk);
        }

        let plaintext = seal_decrypt(
            &object,
            &IBEUserSecretKeys::BonehFranklinBLS12381(usks),
            Some(&IBEPublicKeys::BonehFranklinBLS12381(pks)),
        )
        .unwrap();
        assert_eq!(
            String::from_utf8(plaintext.clone()).unwrap(),
            e["plaintext"].as_str().unwrap(),
        );
        // The repo's identity convention: id == sha256(plaintext).
        let digest: [u8; 32] = Sha256::digest(&plaintext).into();
        assert_eq!(digest.as_slice(), object.id.as_slice());
    }
